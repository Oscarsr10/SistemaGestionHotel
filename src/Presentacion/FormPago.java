    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        if (txtiva.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Tienes que ingresar el iva del comprobante de pago a generar");
            txtiva.requestFocus();
            return;
        }
        if (txttotal_pago.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Tienes que ingresar el total de pago del comprobante");
            txttotal_pago.requestFocus();
            return;
        }
        if (txtnum_comprobante.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Tienes que ingresar un número de comprobante del pago");
            txtnum_comprobante.requestFocus();
            return;
        }

        BPago dts = new BPago();
        FPago funcion = new FPago();
        
        dts.setIdreserva(Integer.parseInt(txtidreserva.getText()));

        int seleccionado = cbotipo_comprobante.getSelectedIndex();
        dts.setTipo_comprobante((String) cbotipo_comprobante.getItemAt(seleccionado));
        
        dts.setNum_comprobante(txtnum_comprobante.getText());
        dts.setIva(Double.parseDouble(txtiva.getText()));
        dts.setTotal_pago(Double.parseDouble(txttotal_pago.getText()));
        
        Calendar calendario;
        int d,m,a;
        
        calendario=dcfecha_pago.getCalendar();
        d=calendario.get(Calendar.DAY_OF_MONTH);
        m=calendario.get(Calendar.MONTH);
        a=calendario.get(Calendar.YEAR) - 1900;
        
        dts.setFecha_pago( new Date (a,m,d));
        
        
        calendario=dcfecha_emision.getCalendar();
        d=calendario.get(Calendar.DAY_OF_MONTH);
        m=calendario.get(Calendar.MONTH);
        a=calendario.get(Calendar.YEAR) - 1900;
        
        dts.setFecha_emision(new Date (a,m,d));

        if (accion.equals("guardar")){
            if (funcion.insertar(dts)){
                JOptionPane.showMessageDialog(rootPane, " El pago por €. " + txttotal_pago.getText() + 
                        " del Sr. " + txtcliente.getText() + " se ha realizado con éxito");
                listar(idreserva);
                inhabilitar();
                
                //Desocupar la Habitación
                FHabitacion funcion2 = new FHabitacion();
                BHabitacion dts2 = new BHabitacion();
                
                dts2.setIdhabitacion(Integer.parseInt(txtidhabitacion.getText()));
                funcion2.desocupar(dts2);
                
                //Cancelar o pagar la reserva
                FReserva funcion3 = new FReserva();
                BReserva dts3 = new BReserva();
                
                dts3.setIdreserva(Integer.parseInt(txtidreserva.getText()));
                funcion3.pagar(dts3);
                
                
            }
        }
        else if (accion.equals("editar")){
            dts.setIdpago(Integer.parseInt(txtidpago.getText()));

            if (funcion.editar(dts)){
                JOptionPane.showConfirmDialog(rootPane, "El pago del Sr. " + 
                        txtcliente.getText() + " se ha editado correctamente");
                listar(idreserva);
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

