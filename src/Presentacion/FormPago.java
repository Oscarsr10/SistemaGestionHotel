    void listar (String buscar){
        try {
            DefaultTableModel modelo;
            FPago funcion = new FPago();
            modelo = funcion.listar(buscar);
            
            tablalistado.setModel(modelo);
            ocultar_columnas();
            lbltotalregistros.setText("Total Pagos " + Integer.toString(funcion.registrostotales));
            
            FConsumo funcion2 = new FConsumo();
            modelo= funcion2.listar(buscar);
            tablalistadoconsumo.setModel(modelo);
            ocultar_columnasconsumo();
            
            lbltotalregistrosconsumo.setText("Total Consumos " + funcion2.registrostotales);
            lbltotalconsumo.setText("Consumo Total: €." + funcion2.consumostotales );
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(rootPane, e);
        }
    }


