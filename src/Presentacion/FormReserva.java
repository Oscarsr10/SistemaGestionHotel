    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        if (!txtidreserva.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "¿Seguro que quieres eliminar la reserva?","Confirmar",2);

            if (confirmacion==0) {
                FReserva funcion = new FReserva();
                BReserva dts = new BReserva();

                dts.setIdreserva(Integer.parseInt(txtidreserva.getText()));
                funcion.eliminar(dts);
                listar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btneliminarActionPerformed

