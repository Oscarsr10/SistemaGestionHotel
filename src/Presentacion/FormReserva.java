    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        if (txtidhabitacion.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Tienes que seleccionar una habitación");
            txtidhabitacion.requestFocus();
            return;
        }
        if (txtcliente.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Tienes que seleccionar un cliente");
            txtcliente.requestFocus();
            return;
        }
        if (txtcosto_alojamiento.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Tienes que ingresar un precio de alojamiento");
            txtcosto_alojamiento.requestFocus();
            return;
        }

        BReserva dts = new BReserva();
        FReserva funcion = new FReserva();

        dts.setIdhabitacion(Integer.parseInt(txtidhabitacion.getText()));
        dts.setIdcliente(Integer.parseInt(txtidcliente.getText()));
        dts.setIdtrabajador(idusuario);
        
        int seleccionado = cbotipo_reserva.getSelectedIndex();
        dts.setTipo_reserva((String) cbotipo_reserva.getItemAt(seleccionado));
        
        Calendar calendar;
        int d,m,a;
        calendar=dcfecha_reserva.getCalendar();
        d=calendar.get(Calendar.DAY_OF_MONTH);
        m=calendar.get(Calendar.MONTH);
        a=calendar.get(Calendar.YEAR) - 1900;
        dts.setFecha_reserva(new Date(a,m,d));
        
        calendar=dcfecha_llegada.getCalendar();
        d=calendar.get(Calendar.DAY_OF_MONTH);
        m=calendar.get(Calendar.MONTH);
        a=calendar.get(Calendar.YEAR) - 1900;
        dts.setFecha_llegada(new Date(a,m,d));
        
        calendar=dcfecha_salida.getCalendar();
        d=calendar.get(Calendar.DAY_OF_MONTH);
        m=calendar.get(Calendar.MONTH);
        a=calendar.get(Calendar.YEAR) - 1900;
        dts.setFecha_salida(new Date(a,m,d));
        
        dts.setCosto_alojamiento(Double.parseDouble(txtcosto_alojamiento.getText()));
        
        seleccionado = cboestado.getSelectedIndex();
        dts.setEstado((String) cboestado.getItemAt(seleccionado));

        if (accion.equals("guardar")){
            if (funcion.insertar(dts)){
                JOptionPane.showConfirmDialog(rootPane, "La reserva se ha registrado correctamente");
                listar("");
                inhabilitar();
                
                //ocupamos la Habitación alquilada
                FHabitacion funcion3= new FHabitacion();
                BHabitacion dts3 = new BHabitacion();
                
                dts3.setIdhabitacion(Integer.parseInt(txtidhabitacion.getText()));
                funcion3.ocupar(dts3);
            }
        }
        else if (accion.equals("editar")){
            dts.setIdreserva(Integer.parseInt(txtidreserva.getText()));
            dts.setIdtrabajador(Integer.parseInt(txtidtrabajador.getText()));

            if (funcion.editar(dts)){
                JOptionPane.showConfirmDialog(rootPane, "La reserva se ha editado correctamente");
                listar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

